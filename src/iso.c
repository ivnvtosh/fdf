#include "fdf.h"
#include <math.h>

t_vector2	iso(t_vector2 old)
{
	t_vector2	new;

	new.x = (old.x - old.y) * cos(1);
	new.y = (old.x + old.y) / 2 * sin(0.8);
	return (new);
}
